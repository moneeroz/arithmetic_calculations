const calculate = {
  // Addition
  add2Nums: (num1, num2) => {
    return num1 + num2
  },
  // Subtraction
  sub2Nums: (num1, num2) => {
    return num1 - num2
  },
  // Multiplication
  multi2Nums: (num1, num2) => {
    return num1 * num2
  },
  // Divison
  div2Nums: (num1, num2) => {
    return num1 / num2
  }
}

module.exports = calculate
